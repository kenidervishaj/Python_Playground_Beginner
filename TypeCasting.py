# type casting = converting the data type of a value to another data type

x = 1 # int
y = 2.0 # float
z = "3" # str

x = float(x) # convert int to float
y = int(y) # convert float to int permenantly
z = float(z) # convert str to int

print(x)
print(int(y)) # convert float to int temporarily
print(y)
print(z)

# Type casting is useful to print two different data types together


